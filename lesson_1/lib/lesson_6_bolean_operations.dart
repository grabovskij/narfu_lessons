/// Логические операторы
void main() {
  bool a = true;
  bool b = false;

  bool andResult = a && b; // Логическое И
  bool orResult = a || b; // Логическое ИЛИ
  bool notResult = !a; // Логическое НЕ

  print('AND Result: $andResult');
  print('OR Result: $orResult');
  print('NOT Result: $notResult');
}
