/// Типы данных
void main() {
  // Целочисленный тип данных
  int quantity = 100;

  // Число с плавающей точкой
  double price = 29.99;

  // Строка
  String product = 'Laptop';

  // Логический тип данных
  bool inStock = true;

  print(
    'Product: $product, Quantity: $quantity, Price: $price, In Stock: '
    '$inStock',
  );
}
